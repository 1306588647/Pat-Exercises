#include <stdio.h>
int main()
{
	int m, n;
	int flag = 1;
	while (scanf("%d %d", &m, &n) != EOF)
	{
		if (n != 0)					//当指数为0，即为常数时，求导后为0，所以什么也不输出
		{
			if (flag == 1)				//输出第一项
			{
				printf("%d %d", m * n, n - 1);
				flag = 0;
			}
			else						//输出后面几项
			{
				printf(" %d %d", m * n, n - 1);
			}
		}
	}
	if (flag == 1)						//如果只输入了一个常数项，那么直接输出0 0
		printf("0 0");
	return 0;
}



